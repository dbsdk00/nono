print(10, "의 절댓값 : ", abs(10))
print(-10, "의 절댓값 : ", abs(-10))
print(128, "의 2진수 : ", bin(128))     # 2진수 (Binary)
print(7, "의 8진수 : ", oct(7))         # 8진수 (Octal)
print(15, "의 16진수 : ", hex(15))      # 16진수 (Hexadecimal)

num = [1, 35, 3, 22, 73]
print("이중 가장 큰 값은 : ", max(num))
print("이중 가장 작은 값은 : ", min(num))
print(f"{num}의 합계는 : ", sum(num))
print("2의 10승은 : ", pow(2, 10))